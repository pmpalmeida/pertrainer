class TrainersController < ApplicationController

  def index         # GET /trainers
  end

  def show          # GET /trainers/:id
  end

  def new           # GET /trainers/new
  end

  def create        # POST /trainers
  end

  def edit          # GET /trainers/:id/edit
  end

  def update        # PATCH /trainers/:id
  end

  def destroy       # DELETE /trainers/:id
  end

end
